//pop > fim do Array
let arr = [1,2,3,4,5]
let remover = arr.pop()
console.log(arr)
console.log(remover)

//shift > começo do array
let nomes = ["Raphael", "Afonso", "João"]
let nome = nomes.shift()
console.log(nomes)
console.log(nome)

// splice > Indice do elemento
let numbers = [1,2,3,4,5]
let number = numbers.splice(1,2)
console.log(numbers)
console.log(number)